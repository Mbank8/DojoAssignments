var days = 60; 

while (days >= 30){
    console.log(days + " days until my birthday... Such a long time :(")

    days -= 1;
}
while (days < 30 && days > 5){
    console.log(days + " days until my birthday! we are almost there!")

    days -= 1;
}
while (days <= 5 && days > 1){
    console.log(days + " DAYS UNTIL MY BIRTHDAY! WOOOOOO!!!")

    days -= 1;
}

while (days === 1){
    console.log(days + " DAY UNTIL MY BIRTHDAY! WOOOOOOOO!!!!!")

    days -= 1;
}

console.log("HAPPY BIRTHDAY PARTY TIMEEEEEE!!!");